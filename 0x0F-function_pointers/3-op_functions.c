#include "3-calc.h"

/**
 * op_add - performs an addition operation
 * @a: integer argument
 * @b: integer argument
 *
 * Return: the sum of the arguments.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - performs a subtraction operation
 * @a: integer argument
 * @b: integer argument
 *
 * Return: the difference of the arguments.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - performs a multiplication operation
 * @a: integer argument
 * @b: integer argument
 *
 * Return: the result of the multiplication.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - performs a division operation
 * @a: integer argument
 * @b: integer argument
 *
 * Return: the result of the division.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - performs a modulo operation
 * @a: integer argument
 * @b: integer argument
 *
 * Return: the result of the modulo operation.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
